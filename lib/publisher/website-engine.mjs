export async function buildHtmlFromText(options) {
  const text = options.text;
  const inputPath = options.inputPath || "input.md";
  const pageType = options.type || "product-website";
  const templateName = options.template || "website-01-fui";

  if (typeof text !== "string" || !text.trim()) throw new Error("缺少输入文本：text");
  if (!["product-website", "product_website"].includes(pageType)) {
    throw new Error(`第一版只支持产品官网：${pageType}`);
  }

  const template = await loadTemplate(templateName);
  assertTemplateMatchesPageType(pageType, template);
  const content = buildContentFromMarkdown(text, inputPath);
  const $ = cheerio.load(template.html, { decodeEntities: false });

  $("title").text(`${content.title} - AI HTML Publisher`);
  removeFakeLinks($);

  if (["website-02-soft-surrealism", "soft-surrealism"].includes(templateName)) {
    injectSoftSurrealism($, content);
  } else if (["website-03-red-clay", "red-clay"].includes(templateName)) {
    injectRedClay($, content);
  } else if (["website-04-premium-midnight", "premium-midnight"].includes(templateName)) {
    injectPremiumMidnight($, content);
  } else if (["website-05-voltflow-cyber-saas", "voltflow-cyber-saas", "cyber-saas"].includes(templateName)) {
    injectVoltflowCyberSaas($, content);
  } else if (["website-07-blueprint-agent-platform", "blueprint-agent-platform", "blueprint-agent"].includes(templateName)) {
    injectBlueprintAgentPlatform($, content);
  } else if (["website-08-editorial-apple-tech", "editorial-apple-tech", "apple-tech"].includes(templateName)) {
    injectEditorialAppleTech($, content);
  } else if (["website-09-blue-shift-portfolio", "blue-shift-portfolio", "blue-shift"].includes(templateName)) {
    injectBlueShiftPortfolio($, content);
  } else {
    injectFi($, content);
  }

  stripTemplateEditorArtifacts($);
  sanitizeGeneratedOutput($);
  cleanKnownInternalRemnants($);
  await inlineTailwindStyles($);
  assignEditIds($);
  const templateId = template.config.id || template.name || templateName;
  const themeColor = templateThemeColor(template.config, templateId);
  const themeConfig = templateThemeConfig(template.config, themeColor);
  injectRuntime($, content.title, templateId, themeColor, themeConfig);

  return $.html();
}

function assertTemplateMatchesPageType(pageType, template) {
  if (["product-website", "product_website"].includes(pageType) && template.kind !== "website") {
    throw new Error(`模板类型不匹配：产品官网只能使用 website 模板，当前模板是 ${template.name}`);
  }
}

