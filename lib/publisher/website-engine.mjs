async function inlineTailwindStyles($) {
  const tailwindScripts = $('script[src^="https://cdn.tailwindcss.com"]');
  if (!tailwindScripts.length) return;

  const tempDir = await mkdtemp(path.join(tmpdir(), "ai-html-publisher-tailwind-"));
  const contentPath = path.join(tempDir, "content.html");
  const inputPath = path.join(tempDir, "input.css");
  const outputPath = path.join(tempDir, "tailwind.css");
  const configPath = path.join(tempDir, "tailwind.config.cjs");

  try {
    await writeFile(contentPath, $.html(), "utf8");
    await writeFile(inputPath, "@tailwind base;\n@tailwind components;\n@tailwind utilities;\n", "utf8");
    await writeFile(configPath, tailwindConfigSource($, contentPath), "utf8");
    await execFileAsync(process.execPath, [
      TAILWIND_CLI_PATH,
      "-c",
      configPath,
      "-i",
      inputPath,
      "-o",
      outputPath,
      "--minify"
    ], { cwd: process.cwd(), maxBuffer: 20 * 1024 * 1024 });

    const css = await readFile(outputPath, "utf8");
    tailwindScripts.remove();
    removeTailwindConfigScripts($);
    $("head").append(`\n<style data-ahp-inlined-tailwind="true">${css}</style>`);
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }
}

function tailwindConfigSource($, contentPath) {
  const configLiteral = extractTailwindConfigLiteral($) || "{}";
  return [
    `"use strict";`,
    `const config = ${configLiteral};`,
    `config.content = [${JSON.stringify(contentPath)}];`,
    `module.exports = config;`
  ].join("\n");
}

function extractTailwindConfigLiteral($) {
  let literal = "";
  $("script:not([src])").each((_, element) => {
    if (literal) return;
    const code = $(element).html() || "";
    const assignment = code.indexOf("tailwind.config");
    if (assignment === -1) return;
    const start = code.indexOf("{", assignment);
    if (start === -1) return;
    literal = readBalancedObjectLiteral(code, start);
  });
  return literal;
}

function readBalancedObjectLiteral(source, start) {
  let depth = 0;
  let quote = "";
  let escaped = false;
  for (let index = start; index < source.length; index += 1) {
    const char = source[index];
    if (quote) {
      if (escaped) escaped = false;
      else if (char === "\\") escaped = true;
      else if (char === quote) quote = "";
      continue;
    }
    if (char === '"' || char === "'" || char === "`") {
      quote = char;
      continue;
    }
    if (char === "{") depth += 1;
    if (char === "}") {
      depth -= 1;
      if (depth === 0) return source.slice(start, index + 1);
    }
  }
  return "{}";
}

