function replaceTextFragments($, replacements, scriptReplacements = []) {
  $("body")
    .find("*")
    .addBack()
    .contents()
    .each((_, node) => {
      if (node.type !== "text" || !node.data) return;
      let value = node.data;
      replacements.forEach(([from, to]) => {
        value = value.split(from).join(to);
      });
      node.data = value;
    });

  $("script:not([src])").each((_, element) => {
    const node = $(element);
    let value = node.html();
    scriptReplacements.forEach(([from, to]) => {
      value = value.split(from).join(to);
    });
    node.html(value);
  });
}

